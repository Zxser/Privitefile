package com.dh.taiyi.output;

import java.util.List;

import com.alibaba.fastjson.annotation.JSONField;

public class DecodeRawTransaction extends CfosOutput<DecodeRawTransaction> {

	@JSONField(name = "hex")
	private String hex;

	@JSONField(name = "txid")
	private String txid;

	@JSONField(name = "version")
	private Long version;

	@JSONField(name = "locktime")
	private Long locktime;

	@JSONField(name = "vin")
	private List<RawTransactionIn> vin;

	@JSONField(name = "vout")
	private List<RawTransactionOut> vout;

	@JSONField(name = "blockhash")
	private String blockhash;

	@JSONField(name = "confirmations")
	private Long confirmations;

	@JSONField(name = "time")
	private Long time;

	@JSONField(name = "blocktime")
	private Long blocktime;

	public String getHex() {
		return result.hex;
	}

	public String getTxid() {
		return result.txid;
	}

	public Long getVersion() {
		return result.version;
	}

	public Long getLocktime() {
		return result.locktime;
	}

	public List<RawTransactionIn> getVin() {
		return result.vin;
	}

	public List<RawTransactionOut> getVout() {
		return result.vout;
	}

	public String getBlockhash() {
		return result.blockhash;
	}

	public Long getConfirmations() {
		return result.confirmations;
	}

	public Long getTime() {
		return result.time;
	}

	public Long getBlocktime() {
		return result.blocktime;
	}

	public void setHex(String hex) {
		this.hex = hex;
	}

	public void setTxid(String txid) {
		this.txid = txid;
	}

	public void setVersion(Long version) {
		this.version = version;
	}

	public void setLocktime(Long locktime) {
		this.locktime = locktime;
	}

	public void setVin(List<RawTransactionIn> vin) {
		this.vin = vin;
	}

	public void setVout(List<RawTransactionOut> vout) {
		this.vout = vout;
	}

	public void setBlockhash(String blockhash) {
		this.blockhash = blockhash;
	}

	public void setConfirmations(Long confirmations) {
		this.confirmations = confirmations;
	}

	public void setTime(Long time) {
		this.time = time;
	}

	public void setBlocktime(Long blocktime) {
		this.blocktime = blocktime;
	}

}
